// Create an array with multiple elements.
// Use the pop() method to remove the last element from the array.
// Use the shift() method to remove the first element from the array.
// Log the array to the console to see the updated elements.

const array = [20,10,30,40,50,60]; //created an array with the elements: 20,10,30,40,50,60
array.pop(); //used pop to remove the last element from the array which is 50
array.shift() //used shift to remove the first element which is 20
console.log(array); //final output is 10,30,40,50
